var setMaxListeners = require('./methods/setMaxListeners');
var getMaxListeners = require('./methods/getMaxListeners')(EventEmitter);
var emit = require('./methods/emit');
var addListener = require('./methods/addListener')(EventEmitter);
var objectCreate = require('./methods/objectCreate');
var once = require('./methods/once');
var prependOnceListener = require('./methods/prependOnceListener');
var removeListener = require('./methods/removeListener');
var removeAllListeners = require('./methods/removeAllListeners');
var listeners = require('./methods/listeners');
var listenerCount = require('./methods/listenerCount');
var eventNames = require('./methods/eventNames');

function EventEmitter() {
  if (!this._events || !Object.prototype.hasOwnProperty.call(this, '_events')) {
    this._events = objectCreate(null);
    this._eventsCount = 0;
  }

  this._maxListeners = this._maxListeners || undefined;
}
module.exports = EventEmitter;

// Backwards-compat with node 0.10.x
EventEmitter.EventEmitter = EventEmitter;

EventEmitter.prototype._events = undefined;
EventEmitter.prototype._maxListeners = undefined;

// By default EventEmitters will print a warning if more than 10 listeners are
// added to it. This is a useful default which helps finding memory leaks.
var defaultMaxListeners = 10;

var hasDefineProperty;
try {
  var o = {};
  if (Object.defineProperty) Object.defineProperty(o, 'x', { value: 0 });
  hasDefineProperty = o.x === 0;
} catch (err) { hasDefineProperty = false }
if (hasDefineProperty) {
  Object.defineProperty(EventEmitter, 'defaultMaxListeners', {
    enumerable: true,
    get: function () {
      return defaultMaxListeners;
    },
    set: function (arg) {
      // check whether the input is a positive number (whose value is zero or
      // greater and not a NaN).
      if (typeof arg !== 'number' || arg < 0 || arg !== arg)
        throw new TypeError('"defaultMaxListeners" must be a positive number');
      defaultMaxListeners = arg;
    }
  });
} else {
  EventEmitter.defaultMaxListeners = defaultMaxListeners;
}

// Obviously not all Emitters should be limited to 10. This function allows
// that to be increased. Set to zero for unlimited.
EventEmitter.prototype.setMaxListeners = setMaxListeners;

EventEmitter.prototype.getMaxListeners = getMaxListeners;

EventEmitter.prototype.emit = emit;

EventEmitter.prototype.addListener = function (type, listener) {
  return addListener(this, type, listener, false);
};

EventEmitter.prototype.on = EventEmitter.prototype.addListener;

EventEmitter.prototype.prependListener = function prependListener(type, listener) {
  return addListener(this, type, listener, true);
};

EventEmitter.prototype.once = once;

EventEmitter.prototype.prependOnceListener = prependOnceListener;

EventEmitter.prototype.removeListener = removeListener;

EventEmitter.prototype.removeAllListeners = removeAllListeners;

EventEmitter.prototype.listeners = listeners;

EventEmitter.listenerCount = function (emitter, type) {
  if (typeof emitter.listenerCount === 'function') {
    return emitter.listenerCount(type);
  } else {
    return listenerCount.call(emitter, type);
  }
};

EventEmitter.prototype.listenerCount = listenerCount;

EventEmitter.prototype.eventNames = eventNames;

